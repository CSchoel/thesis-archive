<node TEXT="reentry" ID="ID_647799589" CREATED="1453796899976" MODIFIED="1453797394451"><richcontent TYPE="DETAILS">

<html>
  <head>
    
  </head>
  <body>
    <ul>
      <li>
        reason: conducting pathway stimulated prematurely
      </li>
      <li>
        can occur at any &quot;loop&quot; in conduction system (e.g. different branches of Purkinje fibers)
      </li>
      <li>
        part of one branch may have a unidirectional block
      </li>
      <li>
        normal behavior: signals from two sides cancel each other out
      </li>
      <li>
        with block: signal from one side can &quot;loop around&quot; to the other side

        <ul>
          <li>
            if loop cycle period is larger than refractory period
          </li>
        </ul>
      </li>
      <li>
        can lead to rapid cyclical activations =&gt; tachycardia
      </li>
    </ul>
  </body>
