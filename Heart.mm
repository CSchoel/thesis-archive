  </head>
  <body>
    <ul>
      <li>
        AP generated by SA node spreads cell-to-cell across atria
      </li>
      <li>
        some evidence for specialized myocytes that act as conducting pathways (internodal tracts)
      </li>
      <li>
        atria and ventricles are separated by non-conducting tissue
      </li>
      <li>
        =&gt; signal can only travel through the atrioventricular node (AV node)
      </li>
      <li>
        signal reaches ventricle through bundle of His into bundle branches
      </li>
      <li>
        bundle branches divide into Purkinje fibers
      </li>
      <li>
        Purkinje fibers connect to ventricular myocytes
      </li>
      <li>
        =&gt; near synchronous depolarization of ventricular myocytes is permitted

        <ul>
          <li>
            needed to generate pressure efficiently
          </li>
          <li>
            disturbances can lead to arrhythmia
          </li>
        </ul>
      </li>
    </ul>
  </body>
