<node TEXT="tissue hypoxia" ID="ID_1208653136" CREATED="1453824246269" MODIFIED="1453824646771"><richcontent TYPE="DETAILS">

<html>
  <head>
    
  </head>
  <body>
    <ul>
      <li>
        leads to membrane depolarization
      </li>
      <li>
        ATP levels decline
      </li>
      <li>
        =&gt; net loss of K+

        <ul>
          <li>
            leaks out through K_ATP channels (normally inhibited by ATP)
          </li>
          <li>
            decreased activity of Na+/K+-ATPAse pump
          </li>
        </ul>
      </li>
      <li>
        increased extracellular K+, decreased intracellular K+
      </li>
      <li>
        =&gt; membrane depolarization
      </li>
      <li>
        =&gt; inactivation of fast sodium channels
      </li>
      <li>
        =&gt; AP upstroke velocity is decreased
      </li>
      <li>
        =&gt; reduced conduction velocity
      </li>
      <li>
        =&gt; reentry currents and tachycardia
      </li>
      <li>
        pacemaker activity is altered
      </li>
      <li>
        latent pacemakers may become active

        <ul>
          <li>
            =&gt; changes in rhythm and ectopic beats
          </li>
        </ul>
      </li>
      <li>
        intracellular calcium accumulates

        <ul>
          <li>
            =&gt; afterdepolarizations and tachycardia
          </li>
        </ul>
      </li>
    </ul>
